syms t w
f = exp(-t^2); % Исходная функция f(t)
F_w = fourier(f, t, w); % Преобразование Фурье исходной функции

% Применим умножение на t^n
f_tn = t * f; % t * f(t)
F_tn_w = fourier(f_tn, t, w); % Преобразование Фурье функции t * f(t)

% Ожидаемый результат: i * d/dw * F(w)
F_expected = 1i * diff(F_w, w); % производная от F(w)

% Выводим результаты для проверки
disp('Преобразование Фурье исходной функции:');
disp(F_w);

disp(['Преобразование Фурье функции t * f(t):']);
disp(F_tn_w);

disp(['Ожидаемый результат (i * d/dw * F(w)):']);
disp(F_expected);

% Сравним результаты
simplify(F_tn_w - F_expected)